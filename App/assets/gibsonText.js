export function RandomText() {
  const words = () => {
    let wordOne = gibsonText[Math.trunc(Math.random() * 160)];
    let wordTwo = gibsonText[Math.trunc(Math.random() * 160)];
    let wordThree = gibsonText[Math.trunc(Math.random() * 160)];

    if (wordThree.includes('-') || Math.random() >= 0.666) {
      wordThree = '';
    }

    if (wordOne.startsWith('-')) {
      wordOne = wordOne.replace('-', '');
    }

    if (wordOne.endsWith('-')) {
      return wordOne + wordTwo.replace('-', '') + ' ' + wordThree;
    } else {
      return wordOne + ' ' + wordTwo.replace('-', '') + ' ' + wordThree;
    }
  };
  return <Text style={{color: '#fff'}}>{words()}</Text>;
}

